package com.wzxy.aroundtaxi.pojo;

public class DrAddress {
	
	private String dr_phonenum;
	private String status;
	private double longitude;
	private double latitude;
	/**
	 * @return the dr_phonenum
	 */
	public String getDr_phonenum() {
		return dr_phonenum;
	}
	/**
	 * @param dr_phonenum the dr_phonenum to set
	 */
	public void setDr_phonenum(String dr_phonenum) {
		this.dr_phonenum = dr_phonenum;
	}
	
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	/**
	 * @return the longitude
	 */
	public double getLongitude() {
		return longitude;
	}
	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	/**
	 * @return the latitude
	 */
	public double getLatitude() {
		return latitude;
	}
	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

}
