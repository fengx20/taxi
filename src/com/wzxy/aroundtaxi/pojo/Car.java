package com.wzxy.aroundtaxi.pojo;

public class Car {
	
	private String carnum;
	private String owner_phonenum;
	private String cartype;
	private String carimg;
	private String owner;
	/**
	 * @return the carnum
	 */
	public String getCarnum() {
		return carnum;
	}
	/**
	 * @param carnum the carnum to set
	 */
	public void setCarnum(String carnum) {
		this.carnum = carnum;
	}
	/**
	 * @return the owner_phonenum
	 */
	public String getOwner_phonenum() {
		return owner_phonenum;
	}
	/**
	 * @param owner_phonenum the owner_phonenum to set
	 */
	public void setOwner_phonenum(String owner_phonenum) {
		this.owner_phonenum = owner_phonenum;
	}
	/**
	 * @return the owner
	 */
	public String getOwner() {
		return owner;
	}
	/**
	 * @param owner the owner to set
	 */
	public void setOwner(String owner) {
		this.owner = owner;
	}
	/**
	 * @return the cartype
	 */
	public String getCartype() {
		return cartype;
	}
	/**
	 * @param cartype the cartype to set
	 */
	public void setCartype(String cartype) {
		this.cartype = cartype;
	}
	/**
	 * @return the carimg
	 */
	public String getCarimg() {
		return carimg;
	}
	/**
	 * @param carimg the carimg to set
	 */
	public void setCarimg(String carimg) {
		this.carimg = carimg;
	}

}
